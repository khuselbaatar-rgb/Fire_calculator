let currentLang = 'ru';
function t(key){ return (I18N[currentLang] && I18N[currentLang][key]) || I18N.ru[key] || key; }

function setLang(lang){
  if(!I18N[lang]) return;
  currentLang = lang;
  document.documentElement.lang = lang;
  try { localStorage.setItem('siteLang', lang); } catch(e){}
  document.querySelectorAll('.flag-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if(I18N[lang][k] !== undefined) el.innerHTML = I18N[lang][k];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = el.getAttribute('data-i18n-ph');
    if(I18N[lang][k] !== undefined) el.placeholder = I18N[lang][k];
  });
  document.title = t('pageTitle');
  if(typeof window._renderLastResult === 'function' && window._lastResult) {
    window._renderLastResult();
  }
}
